    F7 = SPECIAL2 + "A"
    F8 = SPECIAL2 + "B"
    F9 = SPECIAL2 + "C"
    F10 = SPECIAL2 + "D"
    # F11 doesn't get captured.
    F12 = SPECIAL1 + b"\xc2\x86".decode()
