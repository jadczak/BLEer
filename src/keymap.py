    HOME = SPECIAL1 + "G"
    DEL = SPECIAL1 + "S"
    END = SPECIAL1 + "O"
    PG_UP = SPECIAL1 + "I"
    PG_DOWN = SPECIAL1 + "Q"
    F1 = SPECIAL2 + ";"
